     private void listar_funcionarios() {
        try {
            funcionarios_DAO objfuncionariosDAO = new funcionarios_DAO();
            DefaultTableModel model = (DefaultTableModel) tabela_funcionarios.getModel();
            model.setNumRows(0);
            ArrayList<Funcionarios> lista = objfuncionariosDAO.pesquisar_Funcionarios();

            for (int num = 0; num < lista.size(); num++) {
                model.addRow(new Object[]{
                    lista.get(num).getCpf(),
                    lista.get(num).getNome()

                });
            }

        } catch (Exception erro) {
            JOptionPane.showMessageDialog(null, "listar_livros" + erro);
        }
    }

