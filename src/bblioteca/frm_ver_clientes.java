private void listar_clientes() {
        try {
            usuarios_DAO objusuariosDAO = new usuarios_DAO();
            DefaultTableModel model = (DefaultTableModel) tabela_clientes.getModel();
            model.setNumRows(0);
            ArrayList<Usuarios> lista = objusuariosDAO.pesquisar_Clientes();

            for (int num = 0; num < lista.size(); num++) {
                model.addRow(new Object[]{
                    lista.get(num).getCpf(),
                    lista.get(num).getNome(),
                    lista.get(num).getNascimento(),
                    lista.get(num).getTelefone(),
                    lista.get(num).getEmail()

                });
            }

        } catch (Exception erro) {
            JOptionPane.showMessageDialog(null, "listar_Clientes" + erro);
        }
    }
    
