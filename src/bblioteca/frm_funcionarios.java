   private void buscar_funcionarios(){
    
    String sql = " select * from funcionarios where cpf_funcionario = " + txt_cpf_funcionarios.getText();
        try
        {
            livros_DAO objlivros_dao = new livros_DAO();
            ResultSet rs = objlivros_dao.executarconsulta(sql);
            while (rs.next())
            {
                txt_cpf_funcionarios.setText("" + rs.getInt("cpf_funcionario"));
                txt_nome_funcionarios.setText("" + rs.getString("nome_funcionario"));
                txt_senha_funcionarios.setText("" + rs.getString("senha_funcionario"));

            }
        }
        catch (Exception erro)
        {
            System.out.println("Problema na busca");
        }

       

    }
   
   
